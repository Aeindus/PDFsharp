namespace MigraDoc.DocumentObjectModel
{
    /// <summary>
    /// Specifies the page orientation.
    /// </summary>
    public enum Orientation
    {
        /// <summary>
        /// Page height is bigger than page width.
        /// </summary>
        Portrait,

        /// <summary>
        /// Page width is bigger than page height.
        /// </summary>
        Landscape
    }
}
